/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#define PLUGIN_BRUSH(RelativePath,...) FSlateImageBrush(FCSharpActionStyle::InContent(RelativePath,".png"),__VA_ARGS__)

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

TSharedPtr<FSlateStyleSet> FCSharpActionStyle::StyleSet = nullptr;
TSharedPtr<ISlateStyle> FCSharpActionStyle::Get() {return StyleSet;}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

FString FCSharpActionStyle::InContent(const FString &RelativePath, const ANSICHAR* Extension) {
	static FString Content = IPluginManager::Get().FindPlugin(CS_PLUGIN_NAME)->GetContentDir();
	return (Content/RelativePath)+Extension;
}

FName FCSharpActionStyle::GetStyleSetName() {
	static FName StyleName(TEXT("CSharpActionStyle"));
	return StyleName;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void FCSharpActionStyle::Initialize() {
	if (StyleSet.IsValid()) {return;}
	//
	const FVector2D Icon16x16(16.0f,16.0f);
	//
	StyleSet = MakeShareable(new FSlateStyleSet(GetStyleSetName()));
	//
	StyleSet->Set("CS.Action.Reimport",new PLUGIN_BRUSH(TEXT("Icons/Script_16x"),Icon16x16));
	//
	FSlateStyleRegistry::RegisterSlateStyle(*StyleSet.Get());
};

void FCSharpActionStyle::Shutdown() {
	if (StyleSet.IsValid()) {
		FSlateStyleRegistry::UnRegisterSlateStyle(*StyleSet.Get());
		ensure(StyleSet.IsUnique());
		StyleSet.Reset();
	}///
}

